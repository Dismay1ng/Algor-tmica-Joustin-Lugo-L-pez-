#include <stdio.h>

int main() {
    // Mensaje básico
    printf("Hola Mundo\n");

    // Salto de línea
    printf("Hola Mundo\n");

    // Mensaje en diferentes posiciones (no se puede hacer directamente, solo en diferentes líneas)
    printf("Hola Munda\n");
    printf("Hola Munde\n");

    // Mensaje de alerta
    printf("Hola Munda\a\n"); // Envía un mensaje de alerta con sonido

    // Poner símbolos al texto
    printf("!Hola Mundo!\n"); // Agregar símbolos al texto

    return 0;
}
